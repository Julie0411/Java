package serie03;

public class Esercizio03 {

    public static void main(String[] args) {

        int a = 3;
        a++;

        System.out.println(a);

        int b = 7;
        a--;

        System.out.println(b);

        /*Il comando "++" serve per aggiungere un unità ad un int.
        Invece il comando "--" serve per diminuire di uno l'int.
        Questi comandi funzionano anche con i double */
        
        int n = 2;
        n++;
        System.out.println("Il valore di n è " + n);

        n--;
        System.out.println("Il valore di n è " + n);

    }

}
